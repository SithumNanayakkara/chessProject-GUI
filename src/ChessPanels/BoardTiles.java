    private void tileColour() 
    {
        if(tileNo < 8 || (tileNo > 15 && tileNo <24)||(tileNo > 31 && tileNo <40)|| (tileNo > 47 && tileNo <56))
        {
            if(tileNo %2 == 0)
            {
                this.setBackground(Color.red);
            }
            else 
            this.setBackground(Color.blue);
        }
        else
        {
            if(tileNo %2 == 0)
            {
                this.setBackground(Color.blue);
            }
            else 
            this.setBackground(Color.red);
        }
        
       
    }
    

